                                    <p>In 2003, Ring member Ed Estrin presented then-president Don Soul with a long lost document of historic significance. Among a pile of papers containing magic instructions and illusion blueprints he had collected was a typed sheet titled "Temporary Authority to operate as an affiliated Ring of the International Brotherhood of Magicians." It is dated December 6, 1948. The paper grants authority to operate as I.B.M. Ring 76 and is signed by C. James McLemore, Supervisor of Rings, I.B.M.</p>
