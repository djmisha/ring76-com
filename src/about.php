                                    <h4>A Period of Dormancy (1953-1958)</h4>
                                    <p>Throughout 1951 and into 1952, Ring 76 continued to grow in membership and its meetings continued to flourish. It was during this period that San Diego was enjoying new growth and a bit of a rebirth. The Korean War had begun and local military installations were reopened; industrial plants to build aircraft and arms opened their doors again throughout the county.</p>
                                    <p>They were all retooling and restaffing for the winds of a cold war that was sweeping over the world. Suddenly there was no longer time for a local magic meeting. No one knows or remembers exactly how it happened in late 1952 and early 53, but the Ring very suddenly lapsed into a period of lethargy. The leaders of the Ring were also at the time leaders in the community and they channeled their talents and interests in directions other than magic. By March of 1953, even fragmented and occasional Ring reports from San Diego stopped appearing in the Linking Ring magazine.</p>
                                    <p>In 1956, the Society of American Magicians opened an Assembly in San Diego and a whole new list of names suddenly began to appear as members of that magic group. Prominent among those names were Pat and Art Hannum, George Edwards, and Sid Gerhart.</p>
