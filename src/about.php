                                    <h4>The Founding Years (1948-1953)</h4>
                                    <p>The first organizational meeting of the Ring was held on December 12, 1948, at Valle's Restaurant, a prominent eatery since the late 1920s. Valle's was located on Kalmia, between 4th and 5th streets in the Uptown area of the city. The restaurant was razed in the 1960s and today is the location for apartments and condos.</p>
                                    <p>Those present were the original signers of the Ring's Charter and included: Tom Oliver, Ted Angell, James E. Thompson, Stewart Wilson, and Kevin Berry. Members included: Howard Valle, Richard Stahl, Howard Chernoff, R.W. Hafler, Edward Harrison and Bill Risling.</p>
                                    <p>Like all good magic meetings, once the first order of business for the evening, selecting the officers, was out of the way those assembled began doing tricks at the dinner table for each other. This caused quite a stir among the patrons of the restaurant. However, one of the owners of the restaurant, Howard Valle, was a member of the new I.B.M. Ring, so nothing was said about the magical disturbance.</p>
