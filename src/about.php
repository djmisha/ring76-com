                                    <h4>Early Meetings</h4>
                                    <p>The second meeting was again held at Valle's Restaurant on January 16th 1949, but it was decided that all subsequent meetings would be held at the various member's homes. Howard Chernoff hosted the first such meeting on the first Monday of the month. February 7, 1949, was the third meeting and was held at the Chernoff home in La Jolla.</p>
                                    <p>Visiting La Jolla for the winter, from the mid-west was I.B.M. member F.W. Thomas. Thomas was a prominent amateur magician who presented an impromptu magic lecture featuring some of his sleight of hand skills to the delight of the new Ring 76 members.</p>
