                                    <h4>Howard Chernoff</h4>
                                    <p>Howard Chernoff was a prominent and influential businessman, known throughout the United States for his charitable efforts. He is best remembered in San Diego history books for his work as a trustee for the San Diego Zoo and being an outspoken member of the San Diego County Grand Jury.</p>
                                    <p>In the early-1970s he single-handedly rounded up an endangered species of small wild horses known as Preswaldsky's Horses, and brought them to the Wild Animal Park in the San Pasqual Valley where they enjoyed such a successful breeding program that today they are no longer on the endangered species list.</p>
                                    <p>During a television news interview in the early 1970s, Chernoff told reporter Jack White that he had had a life long interest in magic. He told White he became a "magic dropout" in 1952, because of new demands on this time in the business world brought on by the Korean War. He added his interest in magic never waned and he hoped to one day again become part of Ring 76. But that wish was never realized.</p>
