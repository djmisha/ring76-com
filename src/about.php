                                    <h4>Honest Sid Gerhart</h4>
                                    <p>After World War II, Navy veteran Cecil "Sid" Gerhart and his new wife settled in San Diego where he opened a neon sign business. Magic occupied all his spare time, as a collector, a teacher and a performer. "Honest" Sid's comedy magic act was that of a traveling medicine show pitchman. Many of the props he used are on display at Jack's Magic Place.</p>
                                    <p>In late summer of 1958, Art Hannum and Sid Gerhart decided they needed another night of magic club meetings every month. The two of them decided to reactivate the old I.B.M. Ring 76 charter and in December 1958, the Ring opened for business again with new leadership and a new period in history. In November 1958, Ring 76 was re-chartered.</p>
                                    <p>Founding member Sid Gerhart was instrumental in keeping the Ring active, through good-times and bad, until his death in 1975. In honor of his efforts, Ring 76 is known as "The Honest Sid Gerhart Ring".</p>
